class Usuario{
  constructor(nome,email,idade){
        this.nome = nome;
        this.email = email;
        this.idade = idade;
  }

    setNome(nome){
        this.nome = nome;
    }

    
    setNome(email){
        this.email = email;
    }

    
    setNome(idade){
        this.idade = idade;
    }

    getNome (){
        return this.nome;
    }

    getEmail (){
        return this.email;
    }

    getIdade (){
        return this.idade;
    }

}

